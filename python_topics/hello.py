# Python Basics: Input, Output, Variables, and Conditions

# Printing a message
print("Welcome to AI Learning!")

# Taking user input
name = input("Enter your name: ")

# Using a variable
print("Hello, " + name + "! Let's learn Python.")

# Conditional statement
age = int(input("Enter your age: "))

if age >= 18:
    print("You are an adult!")
else:
    print("You are a minor.")

# Loop example
for i in range(1, 6):
    print("Counting:", i)
